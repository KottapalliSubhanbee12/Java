//Inheritance in java:(important)
class parent{  //parent or super or derived class
	public void parent_method() {   //parent method
		System.out.println("I AM PARENT METHOD");
	}
}
class Child extends parent{ //CHILD OR SUB CLASS
	public void child_method() {  //child method
		System.out.println("I AM CHILD1 METHOD");
	}
}
class Child1 extends parent{ //child or sub class
	public void child1_method() {
		System.out.println("I AM CHILD1 METHOD");
	}
}
class Child2 extends parent{ //child or sub class
	public void child1_method() {
		System.out.println("I AM CHILD2 METHOD");
	}
}

